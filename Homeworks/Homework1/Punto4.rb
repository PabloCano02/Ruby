#A string is an object from the String class. What are the methods reverse, capitalize, and length for?

#reverse
#This method returns a new string with the characters in reverse order. For example:
nombre = "Juan Pablo"
puts nombre.reverse
#The result is: olbaP nauJ

#capitalize
#This method returns a new string with the first character capitalized and the rest of the
#characters in lowercase. For example:
nombre = "comer"
puts nombre.capitalize
#The result is: Comer

#length
#This method returns the length of the string. For example:
nombre = "PepitoPerez"
puts nombre.length
#The result is: 11
